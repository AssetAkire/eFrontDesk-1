    private void loginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonMouseClicked
        // TODO add your handling code here:
        if(username.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please fill out username");
        }
        else if(password.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please fill out password");
        }
        else if(username.getText().contains("admin") && password.getText().contains("admin123")){
            dashboard dashboardFrame = new dashboard();
            dashboardFrame.setExtendedState(JFrame.MAXIMIZED_BOTH);
            dashboardFrame.setVisible(true);
            dispose();
        }
        else{
            JOptionPane.showMessageDialog(null, "Try Again", "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginButtonMouseClicked

